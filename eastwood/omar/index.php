        <p>The automobile has created an enormous economic impact not just in one area, but globally. It has allowed entire countries to profit off of this new innovation, for instance Saudi Arabia is rich in oil/petroleum which act as the number one source of fuel for automobiles thus increasing the demand and their economic profit. Also the majority manufacturing of automobiles takes place in Germany and China, which has allowed incredible economic growth to both of these countries. Furthermore, unlike horse carriages cars can be improved on and changed overtime to better accommodate societal needs. Moreover cars can be made from a numerous amount of materials and although automobiles are mostly powered by petroleum they can also be powered by other sources of fuel, for instance, electricity. Additionally most areas began adapting to the new means of transportation and began creating roads and parking infrastructure, which would accommodate automobiles. Also, this resulted in an economic growth due to the excess growth in job opportunities such as taxi, bus, truck and hospital drivers. Additionally cars allowed for an individual sense of freedom due to the multitude of places to travel to and to explore in a short distance drive due to the incredible mobility of the automobile. Moreover, new regulations and policies were made revolving around the safety and use of the automobile due to its possible safety hazard, if used irresponsibly. Despite the many benefits of automobiles, negative environmental consequences exist due to the CO2 released from the gas which can be very harmful for the environment. Nonetheless, some solutions were proposed to this issue such as using electric cars. Finally, automobiles were 80% more efficient than horses and and were ranked as the number one best mode of transportation worldwide. This means that in each and every region, one can see the influence that automobiles had on the world, as well as their continued improvement with new 